import Foundation
import UIKit

extension UIColor {
    static func ODGrayColor() -> UIColor {
        return UIColor(red: 151/255, green: 153/255, blue: 155/255, alpha: 1)
    }
    
    static func ODRedColor() -> UIColor {
        return UIColor(red: 223/255, green: 46/255, blue: 45/255, alpha: 1)
    }
}